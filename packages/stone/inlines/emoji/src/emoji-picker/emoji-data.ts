export { EMOJI_KEYWORDS, searchEmojisByKeyword } from '@ink/stone-shared/utils';

// Local storage key for recent emojis
const RECENT_EMOJIS_KEY = 'ink-recent-emojis';
const MAX_RECENT_EMOJIS = 32;

export function getRecentEmojis(): string[] {
  try {
    const stored = localStorage.getItem(RECENT_EMOJIS_KEY);
    if (stored) {
      return JSON.parse(stored);
    }
  } catch {
    // ignore errors
  }
  return [];
}

export function addRecentEmoji(emoji: string): void {
  try {
    const recent = getRecentEmojis();
    const index = recent.indexOf(emoji);
    if (index > -1) {
      recent.splice(index, 1);
    }
    recent.unshift(emoji);
    if (recent.length > MAX_RECENT_EMOJIS) {
      recent.length = MAX_RECENT_EMOJIS;
    }
    localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(recent));
  } catch {
    // ignore errors
  }
}

export function searchEmojis(query: string): string[] {
  if (!query.trim()) return [];
  const results: string[] = [];

  // Simple search: just return emojis from all categories
  // In a production app, you might want to add emoji names/keywords for better search
  for (const category of EMOJI_CATEGORIES) {
    for (const emoji of category.emojis) {
      // Match if emoji contains the query (for direct emoji input)
      if (emoji.includes(query)) {
        results.push(emoji);
      }
    }
    // Limit results
    if (results.length >= 50) break;
  }

  return results;
}

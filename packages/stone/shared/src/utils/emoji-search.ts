/**
 * Search emojis by keyword
 * @param query - The search query
 * @param emojiList - List of emojis to search through
 * @returns Array of matching emojis
 */
export function searchEmojisByKeyword(
  query: string,
  emojiList?: string[]
): string[] {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) return [];

  const results: string[] = [];
  const seen = new Set<string>();

  // If emojiList is provided, search only within that list
  const emojisToSearch = emojiList ?? Object.keys(EMOJI_KEYWORDS);

  for (const emoji of emojisToSearch) {
    if (seen.has(emoji)) continue;

    const keywords = EMOJI_KEYWORDS[emoji];
    if (keywords?.some(k => k.includes(lowerQuery))) {
      results.push(emoji);
      seen.add(emoji);
    }
  }
  return results;
}
